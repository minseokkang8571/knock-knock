<template>
  <div class="container">
    <b-form
      @submit="onSubmit(form)"
      id="input-form"
      class="pt-5 pb-5 pl-3 pr-3 rounded border border-color-grey">
        <p>Title:</p>
        <b-form-input
          id="input-1"
          v-model="form.title"
          type="text"
          required
          placeholder="제목을 입력하세요."
          autocomplete="off"
        ></b-form-input>

        <p>Content:</p>
        <b-form-textarea
          id="input-2"
          v-model="form.contents"
          type="textarea"
          required
          placeholder="이슈내용을 입력하세요."
          rows="15"
          @keydown.tab.prevent="tabber($event)"
          @keydown.ctrl.66="toggleCtrlShortCut"
          @keydown.ctrl.73="toggleCtrlShortCut"
        ></b-form-textarea>
      <div class="d-flex justify-content-end">
        <!-- preview for markdown -->
        <PreviewModal :contents="form.contents" />
        <button
          type="submit"
          class="btn btn-success ml-2 pl-3 pr-3"
        >Submit</button>
      </div>
    </b-form>
    <!-- TO DO::
    v-html 대체할 방법 찾기
     -->
  </div>
</template>

<script>
import PreviewModal from '@/components/modal/PreviewModal'
import http from '@/util/http-common'
import { mapState } from 'vuex'
import '@/assets/styles/github-gist.css'
export default {
  components: {
    PreviewModal
  },
  props: {
    articleTitle: String,
    articleContents: String
  },
  data() {
    return {
      form: {
        title: '',
        contents: ''
      }
    }
  },
  methods: {
    isUpdate() {
      // 수정시 필요한 파라미터를 갱신해주는 함수
      if (this.currentArticleIdx !== null) {
        this.form.idx = this.currentArticleIdx
        this.form.title = this.articleTitle
        this.form.contents = this.articleContents
      }
    },
    onSubmit(payload) {
      event.preventDefault()
      payload.userIdx = this.$store.state.userInfo.idx
      http
        .post('/article/save', payload, null)
        .then((res) => {
          console.log(res)
          this.$router.push(`articles?articleIdx=${res.data.idx}`)
        })
        .catch((err) => {
          console.log(err)
        })
    },
    tabber(event) {
      // 탭을 눌렀을 때, 4space만큼 커서와 내용이 이동하도록 하는 함수
      const text = this.form.contents
      const originalSelectionStart = event.target.selectionStart
      const startText = text.slice(0, originalSelectionStart)
      const endText = text.slice(originalSelectionStart)
      this.form.contents = `${startText}\t${endText}`
      // TO DO:: two space 추가

      event.target.value = this.form.contents
      event.target.selectionEnd = event.target.selectionStart = originalSelectionStart + 1
    },
    toggleCtrlShortCut(event) {
      // ctrl 숏컷을 사용해 커서블록부분을 Bold, Italic처리하는 함수
      const text = this.form.contents
      const selectionStart = event.target.selectionStart
      const selectionEnd = event.target.selectionEnd
      const startText = text.slice(0, selectionStart)
      const targetText = text.slice(selectionStart, selectionEnd)
      const endText = text.slice(selectionEnd)
      let specialChr = ''

      if (selectionStart === selectionEnd) {
        // 커서블록이 없는 경우
        specialChr = ''
      } else if (event.keyCode === 66) {
        // ctrl+b
        specialChr = '**'
      } else if (event.keyCode === 73) {
        // ctrl+i
        specialChr = '*'
      }

      let changedText = ''
      const targetTextLen = targetText.length
      const specialChrLen = specialChr.length
      if (targetText.substr(0, specialChrLen) === specialChr &&
      targetText.substr(targetTextLen - specialChrLen, specialChrLen) === specialChr) {
        // 활성화 상태인 경우, 양쪽 특수문자를 제거
        changedText = targetText.slice(specialChrLen, targetTextLen - specialChrLen)
      } else {
        // 양쪽 특수문자를 삽입
        changedText = specialChr + targetText + specialChr
      }
      this.form.contents = startText + changedText + endText
    }
  },
  computed: {
    ...mapState(['currentArticleIdx'])
  },
  mounted() {
    this.isUpdate()
  }
}
</script>
